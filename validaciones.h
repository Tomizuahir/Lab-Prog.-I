/** \brief Compruena si una cadena es numerica
 *
 * \param str[] Cadena de caracteres a analizar
 * \return 0-No es numerica, 1-Es numerica
 *
 */
int esNumerico(char str[]);

/** \brief Compruena si una cadena es un numero de telefono
 *
 * \param str[] Cadena de caracteres a analizar
 * \return 0-No es telefono, 1-Es telefono
 *
 */
int esTelefono(char str[]);

/** \brief Compruena si una cadena es alfanumerica
 *
 * \param str[] Cadena de caracteres a analizar
 * \return 0-No es alfanumerica, 1-Es alfanumerica
 *
 */
int esAlfaNumerico(char str[]);

/** \brief Compruena si una cadena esta compuesta solo por letras y espacios
 *
 * \param str[] Cadena de caracteres a analizar
 * \return 0-No es solo letras y espacios, 1-Es solo letras y espacios
 *
 */
int esSoloLetras(char str[]);
